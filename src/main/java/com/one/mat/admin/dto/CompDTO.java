package com.one.mat.admin.dto;

public class CompDTO {
	
	private int member_idx;
	private int compType_code;
	private String compType;
	private String comp_content;
	private String photo;
	
	
	
	
	
	
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	public String getComp_content() {
		return comp_content;
	}
	public void setComp_content(String comp_content) {
		this.comp_content = comp_content;
	}
	public int getMember_idx() {
		return member_idx;
	}
	public void setMember_idx(int member_idx) {
		this.member_idx = member_idx;
	}
	public String getCompType() {
		return compType;
	}
	public void setCompType(String compType) {
		this.compType = compType;
	}
	public int getCompType_code() {
		return compType_code;
	}
	public void setCompType_code(int compType_code) {
		this.compType_code = compType_code;
	}
	
	
	
	

}
